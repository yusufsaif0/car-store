<h2>5.Flat Tires</h2>
While most tires become flat after striking an object or being punctured, it’s possible for simple wear and tear to be the main source. Extend your tire's lifespan by keeping them properly rotated, as recommended by your vehicle manufacturer. Generally, rotating tires every 5,000 miles (or when you change your engine oil) is the best advice.
.<br><br>
<h2>6. Brakes Squeaking or Grinding</h2>
Like any other moving part on your car, the brake system is intended to wear out over a designated period of time. Brakes are vital for safe stopping, so when they display any symptoms of issues, like squealing, squeaking or a soft brake pedal – it should be inspected by a professional mechanic as soon as possible. While minor problems can cause squeaking or squealing noises, once the brakes start grinding, it’s a definite sign they need to be replaced..<br><br>


